import android.graphics.Point;

public class Star {

	private Point p;
	private String s;

	/**
	 * @return the p
	 */
	public Point getP() {
		return p;
	}

	/**
	 * @param p
	 *            the p to set
	 */
	public void setP(Point p) {
		this.p = p;
	}

	/**
	 * @return the s
	 */
	public String getS() {
		return s;
	}

	/**
	 * @param s
	 *            the s to set
	 */
	public void setS(String s) {
		this.s = s;
	}

}